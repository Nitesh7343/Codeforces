import java.util.*;
public class B_Make_It_Increasing{
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        while(t-- > 0) {
            int n = sc.nextInt();
            long[] arr = new long[n];
            for(int i = 0; i < n;i++) {
                arr[i] = sc.nextLong();
            }
            
        }
    }
}